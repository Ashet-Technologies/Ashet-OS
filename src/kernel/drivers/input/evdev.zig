/// Maps an EvDev scancode to a mouse button
pub fn mouseFromEvdev(val: u32) ?ashet.abi.MouseButton {
    return switch (val) {
        evdev.BTN.LEFT => .left,
        evdev.BTN.RIGHT => .right,
        evdev.BTN.MIDDLE => .middle,
        evdev.BTN.SIDE => .nav_previous,
        evdev.BTN.EXTRA => .nav_next,
        evdev.BTN.GEAR_UP => .wheel_up,
        evdev.BTN.GEAR_DOWN => .wheel_down,
        else => null,
    };
}

